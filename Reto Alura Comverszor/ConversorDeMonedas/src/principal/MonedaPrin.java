package principal;

public class MonedaPrin {
	private String monInicial;
	private String monFinal;
	private double valorParaConversion;
	
	public String getMonInicial() {
		return monInicial;
	}
	
	public void setMonInicial(String monInicial) {
		this.monInicial = monInicial;
	}
	
	public String getMonFinal() {
		return monFinal;
	}
	public void setMonFinal(String monFinal) {
		this.monFinal = monFinal;
	}
	public double getValorParaConversion() {
		return valorParaConversion;
	}
	public void setValorParaConversion(double valorParaConversion) {
		this.valorParaConversion = valorParaConversion;
	}
	
	
}
